def solution(n):
    # 뒤집힌 3진법 수를 저장할 문자열
    ternary = ""

    # n이 0이 될 때까지 반복
    while n:
        # 3으로 나눈 나머지를 문자열에 추가
        # 일반적인 3진법 변환은 나머지를 거꾸로 모아야 하지만,
        # 이 문제는 3진법을 뒤집어야 하므로 바로 붙여준다.
        ternary += str(n % 3)

        # 다음 자릿수를 구하기 위해 3으로 나눈 몫 저장
        n //= 3

    # 뒤집힌 3진법 문자열을 10진수로 변환하여 반환
    return int(ternary, 3)